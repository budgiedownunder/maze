
namespace Maze.Maui.App.Controls.Pointer
{
    /// <summary>
    /// Represents a type of icon for use as a pointer
    /// </summary>
    public enum Icon
    {
        /// <summary>
        /// Wait
        /// </summary>
        Wait,
        /// <summary>
        /// Arrow
        /// </summary>
        Arrow,
        /// <summary>
        /// Size all
        /// </summary>
        SizeAll,
        /// <summary>
        /// Size either north-east or south-west
        /// </summary>
        SizeNorthEastSouthWest,
        /// <summary>
        /// Size either north or south
        /// </summary>
        SizeNorthSouth,
        /// <summary>
        /// Size either north-west or south-east
        /// </summary>
        SizeNorthWestSouthEast,
        /// <summary>
        /// Size either west or east
        /// </summary>
        SizeWestEast
    }
}
